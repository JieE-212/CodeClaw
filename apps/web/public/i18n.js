let activeLanguage = normalizeLanguage(readStoredLanguage() || globalThis.navigator?.language || "en");

export function initI18n({ select } = {}) {
  if (select) {
    select.value = activeLanguage;
    select.addEventListener("change", () => setLanguage(select.value));
  }
  applyTranslations();
  return { language: activeLanguage };
}

export function setLanguage(language) {
  activeLanguage = normalizeLanguage(language);
  try {
    globalThis.localStorage?.setItem(LANGUAGE_KEY, activeLanguage);
  } catch {}
  applyTranslations();
  globalThis.window?.dispatchEvent(new CustomEvent("codeclaw:languagechange", { detail: { language: activeLanguage } }));
}

export function currentLanguage() {
  return activeLanguage;
}

export function t(key, values = {}) {
  const dictionary = DICTIONARIES[activeLanguage] || DICTIONARIES.en;
  const fallback = DICTIONARIES.en[key] || key;
  const template = dictionary[key] || fallback;
  return template.replace(/\{(\w+)\}/g, (_, name) => values[name] ?? "");
}

function applyTranslations() {
  if (!globalThis.document) return;
  document.documentElement.lang = activeLanguage;
  for (const element of document.querySelectorAll("[data-i18n]")) {
    element.textContent = t(element.dataset.i18n);
  }
  for (const element of document.querySelectorAll("[data-i18n-placeholder]")) {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  }
  for (const element of document.querySelectorAll("[data-i18n-aria-label]")) {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  }
}

function readStoredLanguage() {
  try {
    return globalThis.localStorage?.getItem(LANGUAGE_KEY) || "";
  } catch {
    return "";
  }
}

function normalizeLanguage(language) {
  const value = String(language || "en").toLowerCase();
  if (value.startsWith("zh")) return "zh-CN";
  if (value.startsWith("ru")) return "ru";
  return SUPPORTED_LANGUAGES.includes(language) ? language : "en";
}
